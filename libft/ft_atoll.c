#include "libft.h"

long long	ft_atoll(const char *nptr)
{
	long long	digit;
	int			sign;

	digit = 0;
	sign = 1;
	while ((*nptr >= 9 && *nptr <= 13) || *nptr == ' ')
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			sign = -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		digit = (digit * 10) + (*nptr - '0');
		nptr++;
	}
	return (digit * sign);
}
